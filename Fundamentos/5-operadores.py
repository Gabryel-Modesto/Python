# Operadores
# aritméticos
num1 = int(input("Digite o primeiro número: \n"))
num2 = int(input("Digite o segunda número: \n"))

sum = num1 + num2
sub = num1 - num2
div = num1 / num2
mult = num1 * num2
mod = num1 % num2
exp = num1 ** num2

print(sum)
print(sub)
print(div)
print(mult)
print(mod)
print(exp)

# Operadores de comparação
bigger = num1 > num2
smaller = num1 < num2
equal = num1 == num2
different = num1 != num2
biggerEqual = num1 >= num2
smallerEqual = num1 <= num2

print(bigger)
print(smaller)
print(equal)
print(different)
print(biggerEqual)
print(smallerEqual)

# Operadores de atribuição
num1 += 1
sub -= 1
mult *= 1
div /= 1